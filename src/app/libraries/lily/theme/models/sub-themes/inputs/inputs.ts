/**
 * Describes style properties for Lily input fields.
 */
export interface LilyInputsTheme {
  /**
   * Background color of the input field.
   */
  lilyInputBackgroundColor: string;

  /**
   * Border color of the input field.
   */
  lilyInputBorderColor: string;

  /**
   * Font color of the input field.
   */
  lilyInputFontColor: string;

  /**
   * Background color of the input field when focused.
   */
  lilyInputFocusBackgroundColor: string;

  /**
   * Border color of the input field when focused.
   */
  lilyInputFocusBorderColor: string;

  /**
   * Font color of the input field when focused.
   */
  lilyInputFocusFontColor: string;

  /**
   * Background color of the input field when disabled.
   */
  lilyInputDisabledBackgroundColor: string;

  /**
   * Border color of the input field when disabled.
   */
  lilyInputDisabledBorderColor: string;

  /**
   * Font color of the input field when disabled.
   */
  lilyInputDisabledFontColor: string;

  /**
   * Background color of the input field when hovered.
   */
  lilyInputHoverBackgroundColor: string;

  /**
   * Border color of the input field when hovered.
   */
  lilyInputHoverBorderColor: string;

  /**
   * Font color of the input field when hovered.
   */
  lilyInputHoverFontColor: string;
}
