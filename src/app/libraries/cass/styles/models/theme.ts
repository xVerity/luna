/**
 * Describes a theme for the custom CSS classes offered by the Cass library.
 */
export interface CassTheme {
  /**
   * The background color of the custom CSS classes.
   */
  cassBackgroundColor: string;

  /**
   * The font color of the custom CSS classes.
   */
  cassFontColor: string;

  /**
   * The border color of the custom CSS classes.
   */
  cassBorderColor: string;
}
